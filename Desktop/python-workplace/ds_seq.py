shoplist=['apple','mango','carrot','banana']
name='awyly'
#序列里的一些尝试
print('The first one in the shoplist is',shoplist[0])
print('The first one and the second one in the shoplist are',shoplist[0:2])
print('The first one, the second one and the third one in the shoplist are',shoplist[0:3])
print('all the things in the sequence are',shoplist[:])
print('Item -1 is',shoplist[-1])
print('name is',name[:])
#从某一字符串中切片
print('character 1 to 3 is',name[0:2])
